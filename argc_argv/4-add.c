#include <stdio.h>
#include <stdlib.h>
#include "main.h"
/**
 * main - entry point to summ all arguments
 * @argc: is the number of arguments
 * @argv: is the values of arguments
 * Return: the sum of arguments or error
 */
int main(int argc, char *argv[])
{
	int i, j, sum = 0;

	if (argc == 1)
	{
		printf("0\n");
		return (0);
	}

	for (i = 1; i < argc; i++)
	{
		for (j = 0; argv[i][j] != '\0'; j++)
		{
			if (argv[i][j] < '0' || argv[i][j] > '9')
			{
				printf("Error\n");
				return (1);
			}
		}
		sum += atoi(argv[i]);
	}
	printf("%d\n", sum);
	return (0);
}
